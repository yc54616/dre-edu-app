export interface HallOfFameReview {
  id: string | number;
  name: string;
  content: string;
  tag: string;
  stars: number;
}

export const clampReviewStars = (value: unknown): number => {
  const parsed = Number(value);
  if (!Number.isFinite(parsed)) return 5;
  return Math.max(1, Math.min(5, Math.round(parsed)));
};

